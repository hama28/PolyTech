document.write("<h1>「+」演算子を利用する</h1>");
document.write("<h2>数値を「+」して代入する</h2>");
let ans1 = 500 + 3.14159265;

document.write("<p>変数「500」と「3.14159265」を「+」した「ans1」を表示</p>");
document.write(ans1);

document.write("<h2>数値の入った変数同士を「+」する</h2>");
let num = 500;
let dec =3.14159265;
let ans2 = num + dec;

document.write("<p>◆変数「num」と「dec」を「+」した「ans2」を表示</p>");
document.write(ans2);
