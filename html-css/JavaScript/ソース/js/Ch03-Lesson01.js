document.write('<h1>「+」演算子を利用する</h1>');
document.write('<h2>数値を「+」して代入する</h2>');
let ans1 = 500 + 3.14;      //加算して代入

document.write('<p>◇変数「500」と「3.14」を「+」した「ans1」を表示</p>');
document.write(ans1);       //変数「ans」を表示する

document.write('<h2>数値の入った変数同士を「+」する</h2>');
let num = 500;              //変数宣言と代入
let dec = 3.14;             //変数宣言と代入
let ans2 = num + dec;       //加算して代入する

document.write('<p>◇変数「num」と「dec」を「+」した「ans2」を表示</p>');
document.write(ans2);       //変数「ans2」を表示する