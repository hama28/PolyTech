//変数宣言と同時に初期化する(代入する)
let num = 500;
let dec = 3.14;
let name = "Tendo Yoshimi";
let br = '<br>';

//表示する
document.write('<p>◇変数「num」の表示</p>');
document.write(num);    //整数「num」の表示
document.write(br);     //改行タグの挿入

document.write('<p>◇変数「dec」の表示</p>');
document.write(dec);    //小数「dec」の表示
document.write(br);     //改行タグの挿入

document.write('<p>◇変数「name」の表示</p>');
document.write(name);   //文字列「name」の表示
document.write(br);     //改行タグの挿入