document.write('<h1>インクリメントとデクリメント</h1>');

//インクリメント（自分自身に1加算して代入する）
let num1 = 500;
num1++;

document.write('<p>◇「num1++」を表示する</p>');
document.write(num1);   //変数「num1」を表示する

//インクリメント（自分自身に1減算して代入する）
let num2 = 500;
num2--;

document.write('<p>◇「num2--」を表示する</p>');
document.write(num2);   //変数「num2」を表示する

